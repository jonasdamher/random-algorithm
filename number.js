function getPrefix($, tr, total, count = 0, list = []) {
  if (total > count) {

    let currentTr = $(tr[count])
    let tdCountry = $(currentTr.children('td')[0]);
    let tdPrefix = $(currentTr.children('td')[1]);
    let tdPrefixMobile = $(currentTr.children('td')[2]).text();

    let listPrefix = [];

    if (tdPrefixMobile.search(',') != 1) {
      let splitText = tdPrefixMobile.split(',');

      splitText.forEach((prefix) => {

        if (prefix.search('-') != 1) {
          let splitPrefix = prefix.split('-');

          for (let index = splitPrefix[0]; index <= splitPrefix[1]; index++) {
            listPrefix.push(index.toString());
          }
        } else {
          listPrefix.push(prefix);
        }

      });
    } else {
      listPrefix.push(tdPrefixMobile);
    }

    ++count;

    list.push({
      country: tdCountry.text().replace(/\[.*\]|\(.*\)/g, '').trim(),
      prefix: tdPrefix.text().trim().replace(/[^0-9-]/, ''),
      prefix_mobile: listPrefix.map((prefix) => prefix.replace(/[^0-9-]/, '').trim())
    })

    return getPrefix($, tr, total, count, list);
  } else {
    return list;
  }

}

